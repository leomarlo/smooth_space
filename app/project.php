              <div class="card-body justify_">
                <p>
                  For each pair of artistic practices we investigate how to intensify spatial phenomena,
                  how to translate and communicate them, how to appropriate them for ones own practice and how to engage into mutual composition.
                  We inquire about the kinetic spheres of two performers when a third one enters.
                  Similar to the three-body problem in physics we seek to describe this emergent phenomena.
                  How can the complexified spatial interactions of the kinespheres be notated.
                  How saturated becomes the space, how fragmented, how synchronous?
                </p>
                <p>
                  We conduct experiments during which a smooth space phenomenon is first distilled and
                  condensed to a clear spatial expression. This we call the null-space. It is the starting point
                  for our investigations and experiments, which are co-created in an iterative process by the
                  performers and a complexity scientist. We foresee a recursive pipeline, that divides the work into individual,
                  collision and co-production modi. We also foresee a role fluidity - performer, experimenter and documenter
                  are not attached to individuals allowing for a change of perspective.
                </p>
