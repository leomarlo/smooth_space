                  We explore how to notate, communicate and compose space phenomena across audio-corporeal artistic practices.
                  We investigate these in four disciplines: dance, rhythmics, choir conducting and direct sound.
                  They share an alertness for and a certain tacit knowledge about space.
                  In stark contrast to musical or movement notations,
                  one finds that notated spaces are rather scarce in the audio-corporeal practices even though space unites them.
                  We argue that this lacuna will be bridged by working on an atlas of space qualities.
                  Rather than communicating merely the metric measures of spaces without the performer,
                  we are concerned instead with emergent spatial qualities of smooth spaces that complement the performer,
                  that exist outside of but not without the performer.
                </p>

